let count = 0;

const cartCount = document.getElementById("cart-count");
const buttons = document.querySelectorAll(".cart-btn");
const toast = document.getElementById("toast");

buttons.forEach(button => {
    button.addEventListener("click", (e) => {

        e.stopPropagation();

        count++;
        cartCount.innerText = count;

        const productName = button.closest(".card").querySelector("h4").innerText;

        toast.innerText = `✅ ${productName} added to cart`;
        toast.classList.add("show");

        setTimeout(() => {
            toast.classList.remove("show");
        }, 2000);

    });
});


const searchBox = document.getElementById("search-box");
const cards = document.querySelectorAll(".card");

searchBox.addEventListener("keyup", function () {

    const searchValue = searchBox.value.toLowerCase();

    cards.forEach(card => {

        const productName = card.querySelector("h4").innerText.toLowerCase();

        if (productName.includes(searchValue)) {
            card.style.display = "block";
        } else {
            card.style.display = "none";
        }

    });

});
const themeBtn = document.getElementById("theme-btn");

themeBtn.addEventListener("click", () => {

    document.body.classList.toggle("dark-mode");

});


const modal = document.getElementById("productModal");

const modalImg = document.getElementById("modal-img");
const modalTitle = document.getElementById("modal-title");
const modalPrice = document.getElementById("modal-price");
const modalRating = document.getElementById("modal-rating");

const closeBtn = document.querySelector(".close");

cards.forEach(card=>{

    card.addEventListener("click",()=>{

        modal.style.display="flex";

        modalImg.src=card.querySelector("img").src;
        modalTitle.innerText=card.querySelector("h4").innerText;
        modalPrice.innerText=card.querySelectorAll("p")[0].innerText;
        modalRating.innerText=card.querySelector(".rating").innerText;

    });

});

closeBtn.addEventListener("click",()=>{

    modal.style.display="none";

});

window.addEventListener("click",(e)=>{

    if(e.target===modal){

        modal.style.display="none";

    }

});








